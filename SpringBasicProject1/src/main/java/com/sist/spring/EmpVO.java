package com.sist.spring;
import java.util.*;
//VO: 스프링이 관리하는 클래스가 아니다 ==> 일반 데이터형 (int, ...)임 
/*
*  <Spring에 등록하는 법> 
*   1. XML
*   2. Annotation
*   3. XML+Annotation: 가장 많이 사용됨★ 
*    1) Annotation: 사용자 정의 클래스 
*    2) XML: 라이브러리 등록 ex) MyBatis, JDBC...
*     - 라이브러리는 내가 만든게 아니니까 구조를 모르잖아 그러니까 그 위에 Annotation을 올릴 수 없음 
*     - ==> XML 사용해서 등록  
*/
public class EmpVO {
	private int empno;
	private String ename;
	private String job;
	private int mgr;
	private Date hiredate;
	private int comm;
	private int sal;
	private int deptno;
	
	public int getEmpno() {
		return empno;
	}
	public void setEmpno(int empno) {
		this.empno = empno;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public int getMgr() {
		return mgr;
	}
	public void setMgr(int mgr) {
		this.mgr = mgr;
	}
	public Date getHiredate() {
		return hiredate;
	}
	public void setHiredate(Date hiredate) {
		this.hiredate = hiredate;
	}
	public int getComm() {
		return comm;
	}
	public void setComm(int comm) {
		this.comm = comm;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}
	public int getDeptno() {
		return deptno;
	}
	public void setDeptno(int deptno) {
		this.deptno = deptno;
	}
}
